    private void themNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themNVActionPerformed
        if(evt.getSource()==themNV){
            try {
                oLoiNV.setText("");
                
                int ma=0, soDong=0;
                File file = new File("NV.TXT");
                if(!file.exists()){
                    file.createNewFile();
                }
                Scanner sc= new Scanner(file);
                if(!sc.hasNextLine()){
                    ma=1000;
                }else{
                    while(sc.hasNextLine()){
                        sc.nextLine();
                        soDong++;
                    }
                    ma=soDong/5 +1000;
                }
                sc.close();
                
                if(oTenNV.getText()==null || oTenNV.getText().trim().isEmpty()){
                    oLoiNV.setText("Chua nhap o ten nv");
                    throw new Exception();
                }
                if(oDiaChi.getText()==null || oDiaChi.getText().trim().isEmpty()){
                    oLoiNV.setText("Chua nhap o dia chi");
                    throw new Exception();
                }
                if(oSoDienThoai.getText()==null || oSoDienThoai.getText().trim().isEmpty()){
                    oLoiNV.setText("Chua nhap o so dien thoai");
                    throw new Exception();
                }
                if(oBacLuong.getText()==null || oBacLuong.getText().trim().isEmpty()){
                    oLoiNV.setText("Chua nhap o bac luong");
                    throw new Exception();
                }
                
                FileWriter fw= new FileWriter(file.getAbsoluteFile(),true);
                BufferedWriter bw= new BufferedWriter(fw);
                
                bw.write(ma+"\n");
                bw.write(oTenNV.getText()+"\n");
                bw.write(oDiaChi.getText()+"\n");
                bw.write(Integer.parseInt(oSoDienThoai.getText())+"\n");
                bw.write(Integer.parseInt(oBacLuong.getText())+"\n");
                
                bw.close();
                fw.close();
                showNV();
                
            } catch(NumberFormatException e){
                oLoiNV.setText("Sai dinh dang");
            } catch (Exception e) {
                
            }
        }
    }//GEN-LAST:event_themNVActionPerformed

    private void themPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themPActionPerformed
        if(evt.getSource()==themP){
            try {
                oLoiP.setText("");
                
                int ma=0, soDong=0;
                File file = new File("P.TXT");
                if(!file.exists()){
                    file.createNewFile();
                }
                Scanner sc= new Scanner(file);
                if(!sc.hasNextLine()){
                    ma=100;
                }else{
                    while(sc.hasNextLine()){
                        sc.nextLine();
                        soDong++;
                    }
                    ma=soDong/4 +100;
                }
                sc.close();
                
                if(oTenP.getText()==null || oTenP.getText().trim().isEmpty()){
                    oLoiP.setText("Chua nhap o ten P");
                    throw new Exception();
                }
                if(oMoTa.getText()==null || oMoTa.getText().trim().isEmpty()){
                    oLoiP.setText("Chua nhap o mo ta");
                    throw new Exception();
                }
                if(oHeSo.getText()==null || oHeSo.getText().trim().isEmpty()){
                    oLoiP.setText("Chua nhap o he so");
                    throw new Exception();
                }
                
                if(Integer.parseInt(oHeSo.getText())>20){
                    if(Integer.parseInt(oHeSo.getText())!=0){
                        oLoiP.setText("he so chi tu 1 den 20");
                        throw new Exception();
                    }
                }
                
                FileWriter fw= new FileWriter(file.getAbsoluteFile(),true);
                BufferedWriter bw= new BufferedWriter(fw);
                
                bw.write(ma+"\n");
                bw.write(oTenP.getText()+"\n");
                bw.write(oMoTa.getText()+"\n");
                bw.write(Integer.parseInt(oHeSo.getText())+"\n");
              
                
                bw.close();
                fw.close();
                showP();
                
            } catch(NumberFormatException e){
                oLoiP.setText("Sai dinh dang");
            } catch (Exception e) {
                
            }
        }
    }//GEN-LAST:event_themPActionPerformed

