    public void ghiQuanLy() throws IOException{
        File file= new File("QLNS.TXT");
        if(!file.exists()){
            file.createNewFile();
        }
        FileWriter fw= new FileWriter(file);
        BufferedWriter bw= new BufferedWriter(fw);
        
        for(BangQuanLy s:listQuanLy){
            bw.write(s.getNv().getMaNV()+"\n");
            bw.write(s.getP().getMaP()+"\n");
            bw.write(s.getSoNgayLV()+"\n");
        }
        bw.close();
        fw.close();
    }
    
    public void docQuanLy() throws FileNotFoundException{
        File file= new File("QLNS.TXT");
        listQuanLy.clear();
        if(file.exists()){
            Scanner sc= new Scanner(file);
            
            int maNV=0, maP=0, sl=0;
            while(sc.hasNextLine()){
                maNV= Integer.parseInt(sc.nextLine());
                maP= Integer.parseInt(sc.nextLine());
                sl= Integer.parseInt(sc.nextLine());
                BangQuanLy ql= new BangQuanLy();
                for(NhanVien s:listNV()){
                    if(maNV== s.getMaNV()){
                        ql.setNv(s);
                        break;
                    }
                }
                for(PhongBan s:listP()){
                    if(maP== s.getMaP()){
                        ql.setP(s);
                        break;
                    }
                }
               ql.setSoNgayLV(sl); 
               listQuanLy.add(ql);
            }
            sc.close();
       }
        
    }
    
