    public ArrayList<NhanVien> listNV() throws FileNotFoundException{
        ArrayList<NhanVien> list= new ArrayList<>();
        File file= new File("NV.TXT");
        if(file.exists()){
            Scanner sc= new Scanner(file);
            while(sc.hasNextLine()){
                NhanVien a= new NhanVien();
                a.setMaNV(Integer.parseInt(sc.nextLine()));
                a.setTenNV(sc.nextLine());
                a.setDiaChi(sc.nextLine());
                a.setSoDienThoai(Integer.parseInt(sc.nextLine()));
                a.setBacLuoong(Integer.parseInt(sc.nextLine()));
                list.add(a);
            }
            sc.close();
        }
        return list;
    }
    
