package quanlynhansu;

/**
 *
 * @author quyto
 */
public class PhongBan {
    private int maP;
    private String tenP;
    private String moTa;
    private int heSo;

    public PhongBan() {
    }

    public int getMaP() {
        return maP;
    }

    public String getTenP() {
        return tenP;
    }

    public String getMoTa() {
        return moTa;
    }

    public int getHeSo() {
        return heSo;
    }

    public void setMaP(int maP) {
        this.maP = maP;
    }

    public void setTenP(String tenP) {
        this.tenP = tenP;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }

    public void setHeSo(int heSo) {
        this.heSo = heSo;
    }
    
}
